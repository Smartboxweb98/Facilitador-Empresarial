    public void preenchertabela(String SQL){
        ArrayList dados = new ArrayList();
        
        
        String [] Colunas = new String[]{"TEXTO","USUARIO","DATA","HORARIO","ID"};
        conecta.conectaBanco();
        conecta.executaSQL(SQL);
        try {
           conecta.rs.first();      
        do{
          dados.add(new Object[]{conecta.rs.getString("texto"),conecta.rs.getString("usuario"),conecta.rs.getString("data"),conecta.rs.getString("horario"),conecta.rs.getString("id")});
        }while(conecta.rs.next());
        conecta.desconecta();
        } catch (SQLException ex) {
       //     JOptionPane.showMessageDialog(null, "N√£o existe dados cadastrados!");
       
        }
        
      
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTableChat.setModel(modelo);
        jTableChat.getColumnModel().getColumn(0).setPreferredWidth(400);
        jTableChat.getColumnModel().getColumn(0).setResizable(false);
        jTableChat.getColumnModel().getColumn(1).setPreferredWidth(80);
        jTableChat.getColumnModel().getColumn(1).setResizable(false);
        jTableChat.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTableChat.getColumnModel().getColumn(2).setResizable(false);
        jTableChat.getColumnModel().getColumn(3).setPreferredWidth(80);
        jTableChat.getColumnModel().getColumn(3).setResizable(false);  
        jTableChat.getColumnModel().getColumn(4).setPreferredWidth(40);
        jTableChat.getColumnModel().getColumn(4).setResizable(false);
        jTableChat.getTableHeader().setReorderingAllowed(true);
        jTableChat.setAutoResizeMode(jTableChat.AUTO_RESIZE_OFF);
        jTableChat.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
       
    }
    
