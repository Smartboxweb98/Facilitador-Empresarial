public void preenchertabela(String SQL){
        ArrayList dados = new ArrayList();
        
        
        String [] Colunas = new String[]{"ID","CÓDIGO","EMPRESA","CNPJ","GRUPO"};
        conecta.conectaBanco();
        conecta.executaSQL(SQL);
        try {
          //  conecta.conectaBanco();
           conecta.rs.first();      
        do{
          dados.add(new Object[]{conecta.rs.getInt("id"),conecta.rs.getString("código"),conecta.rs.getString("empresa"),conecta.rs.getString("cnpj"),conecta.rs.getString("grupo")});
        }while(conecta.rs.next());
        conecta.desconecta();
        } catch (SQLException ex) {
       //     JOptionPane.showMessageDialog(null, "Não existe dados cadastrados!");
        }
        
      
        ModeloTabela modelo = new ModeloTabela(dados, Colunas);
        jTableEmpresa.setModel(modelo);
        jTableEmpresa.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTableEmpresa.getColumnModel().getColumn(0).setResizable(false);
        jTableEmpresa.getColumnModel().getColumn(1).setPreferredWidth(70);
        jTableEmpresa.getColumnModel().getColumn(1).setResizable(false);
        jTableEmpresa.getColumnModel().getColumn(2).setPreferredWidth(400);
        jTableEmpresa.getColumnModel().getColumn(2).setResizable(false);
        jTableEmpresa.getColumnModel().getColumn(3).setPreferredWidth(160);
        jTableEmpresa.getColumnModel().getColumn(3).setResizable(false);
        jTableEmpresa.getColumnModel().getColumn(4).setPreferredWidth(160);
        jTableEmpresa.getColumnModel().getColumn(4).setResizable(false);
        jTableEmpresa.getTableHeader().setReorderingAllowed(true);
        jTableEmpresa.setAutoResizeMode(jTableEmpresa.AUTO_RESIZE_OFF);
        jTableEmpresa.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
       
    }
