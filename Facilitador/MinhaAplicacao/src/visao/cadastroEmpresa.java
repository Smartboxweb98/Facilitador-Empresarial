    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        // TODO add your handling code here:
        try {
            conecta.conectaBanco();
            PreparedStatement pst = conecta.conn.prepareStatement("update cadastroempresa set código= ?, empresa=?, cnpj=?, grupo=? where id=?");
            pst.setString(1,jTextFieldCodigo.getText());
            pst.setString(2,jTextFieldEmpresa.getText());
            pst.setString(3,jTextFieldCnpj.getText());
            pst.setString(4,jTextFieldGrupo.getText());
            pst.setInt(5,Integer.parseInt(jTextFieldId.getText()));
            pst.execute();
            JOptionPane.showMessageDialog(rootPane,"Alterado com sucesso");
            //preenchertabela("select * from usuarios order by id");
            preenchertabela("select * from cadastroempresa order by id ");
            conecta.desconecta();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"Erro ao alterar!\n Erro: "+ex);
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

